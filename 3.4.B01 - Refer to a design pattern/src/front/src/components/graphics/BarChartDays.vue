        onClick: (event, elements, chart) => {
          if (elements[0]) {
            const i = elements[0].index
            const editTime = prompt('Enter new time', '')
            if (editTime !== null) {
              chart.data.datasets[0].data[i] = editTime
              chart.update()
            }
          }
        }
      }
    }
  },

  methods: {
  }
}
